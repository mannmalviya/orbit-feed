// Log-scale normalization: 0.5 → 10 000 people/km²
const LOG_MIN = Math.log10(0.5)
const LOG_RANGE = Math.log10(10_000) - LOG_MIN

/** Returns a [0, 1] position on the heatmap scale for a given density value. */
export function densityT(density: number): number {
  const log = Math.log10(Math.max(density, 0.5))
  return Math.min(Math.max((log - LOG_MIN) / LOG_RANGE, 0), 1)
}

type RGB = [number, number, number]

// Heatmap stops: deep-blue → cyan → green → yellow → orange → red
const STOPS: Array<{ t: number; rgb: RGB }> = [
  { t: 0.00, rgb: [20,  40, 165] },
  { t: 0.25, rgb: [0,  155, 225] },
  { t: 0.50, rgb: [0,  200, 100] },
  { t: 0.70, rgb: [255, 210,  0] },
  { t: 0.85, rgb: [255,  90,  0] },
  { t: 1.00, rgb: [210,   0, 20] },
]

export function heatmapRgb(t: number): RGB {
  for (let i = 1; i < STOPS.length; i++) {
    if (t <= STOPS[i].t) {
      const seg = (t - STOPS[i - 1].t) / (STOPS[i].t - STOPS[i - 1].t)
      const a = STOPS[i - 1].rgb
      const b = STOPS[i].rgb
      return [
        Math.round(a[0] + (b[0] - a[0]) * seg),
        Math.round(a[1] + (b[1] - a[1]) * seg),
        Math.round(a[2] + (b[2] - a[2]) * seg),
      ]
    }
  }
  return STOPS[STOPS.length - 1].rgb
}

/** RGBA string for a country's density. Falls back to a dim slate for unknown codes. */
export function densityColor(iso2: string, alpha: number): string {
  const d = POPULATION_DENSITY[iso2]
  if (!d) return `rgba(40, 50, 100, ${alpha * 0.5})`
  const [r, g, b] = heatmapRgb(densityT(d))
  return `rgba(${r}, ${g}, ${b}, ${alpha})`
}

/** Polygon altitude (extrusion) proportional to log-density. */
export function densityAltitude(iso2: string): number {
  const d = POPULATION_DENSITY[iso2]
  if (!d) return 0.004
  return 0.004 + densityT(d) * 0.09
}
